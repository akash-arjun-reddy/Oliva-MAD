class Product {
  final String name;
  final String imageAsset;
  final String price;
  final String description;
  final double rating;
  final int reviewCount;
  
  Product({
    required this.name,
    required this.imageAsset,
    required this.price,
    required this.description,
    required this.rating,
    required this.reviewCount,
  });
} 